# returns a boolean based on the given value's existence in the range
puts (1..20).include?(30)
puts (1..20).include?(15)

# returns the last value or last number of given values
puts (1..20).last
puts (1..20).last(5)

# returns the largest value in range. If larger value is given first returns nil
puts (1..20).max

# returns the minimum value in range
puts (1..20).min
